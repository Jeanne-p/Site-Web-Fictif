L'organisme a été créé en 2016 par trois étudiants du collège Champlain St-Lawrence: Oussama Deziri, Youness Mankari et Irena Belcheva. Ces trois étudiants
du programme de gestion de commerces ont eu la brillante idée de créer cet organisme afin d'aider les aînés ayant de la difficulté à utiliser les nouvelles technologies.
<hr/>
<p><h2 class="text-center">L'organisme</h2>
<i>Connectons Ensemble</i> est un organisme à but non lucratif. Sa mission est d'aider les aînés avec les nouvelles technologies afin qu'ils puissent communiquer
plus facilement avec leur famille et leurs amis.
Chaque année, plusieurs ateliers sont organisés par <i>Connectons Ensemble</i> dans différentes maisons de retraite afin d'initier les résidents aux
 nouvelles technologies. Une équipe de formateurs sont sur place afin de donner des explications claires et répondre aux questions.

